    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try{
        objAviso.setPrecio(BigDecimal.valueOf(Double.valueOf(txtPrecio.getText())));
        objAviso.setFecha(txtFecha.getDate());
        
        List<Medida> lstMedida = DAOMedida.getInstance().listar();
        objAviso.setIdMedida(lstMedida.get(cmbMedida.getSelectedIndex()));
        
        List<Color> lstColor = DAOColor.getInstance().listar();
        objAviso.setIdColor(lstColor.get(cmbColor.getSelectedIndex()));
        
        List<Pagina> lstPagina = DAOPagina.getInstance().listar();
        objAviso.setIdPagina(lstPagina.get(cmbPagina.getSelectedIndex()));
        
        List<Categoria> lstCategoria = DAOCategoria.getInstance().listar();
        objAviso.setIdCategoria(lstCategoria.get(cmbCategoria.getSelectedIndex()));
        
        //modificamos la bd
        boolean r = DAOAviso.getInstance().modificar(objAviso);
        if (r == true){
            JOptionPane.showMessageDialog(this, "Pagina modificada con exito!");
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo modificar la pagina");
        }
        lista.mostrar();
        }catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

