    public ModificarAviso(Aviso objAviso,ListarAviso lista) {
        initComponents();
        this.objAviso = objAviso;
        txtId.setText("" + objAviso.getId());
        txtPrecio.setText(objAviso.getPrecio().toString());
        txtFecha.setDate(objAviso.getFecha());
        
        //ComboBox Medida
        List<Medida> lstMedida = DAOMedida.getInstance().listar();
        String[] lstMedidaC =  new String[lstMedida.size()];
        int i =0,j =0;
        for (Medida item : lstMedida) {
            lstMedidaC[i++] = item.getNombre();
            if(objAviso.getIdMedida().getId()==item.getId())
                j = i-1;
        }
        cmbMedida.setModel(new javax.swing.DefaultComboBoxModel(lstMedidaC));
        cmbMedida.setSelectedIndex(j);
        
        
        //ComboBox Color
        List<Color> lstColor = DAOColor.getInstance().listar();
        String[] lstColorC =  new String[lstColor.size()];
        int a =0,b =0;
        for (Color item : lstColor) {
            lstColorC[a++] = item.getNombre();
            if(objAviso.getIdColor().getId()==item.getId())
                b = a-1;
        }
        cmbColor.setModel(new javax.swing.DefaultComboBoxModel(lstColorC));
        cmbColor.setSelectedIndex(b);
        
        
        //ComboBox Pagina
        List<Pagina> lstPagina = DAOPagina.getInstance().listar();
        String[] lstPaginaC =  new String[lstPagina.size()];
        int c =0,d =0;
        for (Pagina item : lstPagina) {
            lstPaginaC[c++] = item.getNombre();
            if(objAviso.getIdPagina().getId()==item.getId())
                d = c-1;
        }
        cmbPagina.setModel(new javax.swing.DefaultComboBoxModel(lstPaginaC));
        cmbPagina.setSelectedIndex(d);
        
        
        //ComboBox Categoria
        List<Categoria> lstCategoria = DAOCategoria.getInstance().listar();
        String[] lstCategoriaC =  new String[lstCategoria.size()];
        int e =0,f =0;
        for (Categoria item : lstCategoria) {
            lstCategoriaC[e++] = item.getNombre();
            if(objAviso.getIdCategoria().getId()==item.getId())
                f = e-1;
        }
        cmbCategoria.setModel(new javax.swing.DefaultComboBoxModel(lstCategoriaC));
        cmbCategoria.setSelectedIndex(f);
        
        
        this.lista = lista;
    }

