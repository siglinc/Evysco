    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        int index = tblCategoria.getSelectedRow();
        if (index != -1){
            //Modificamos la fila seleccionada
            Categoria objCategoria = listaCategoria.get(index);
            ModificarCategoria ventana = new ModificarCategoria(objCategoria,this);
            dskPrincipal.add(ventana);
            ventana.setVisible(true);
            mostrar();
        }else{
            JOptionPane.showMessageDialog(this,"Se necesita seleccionar una fila!");
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    
    public void  mostrar (){
            //1. Traemos objetos
        listaCategoria = DAOCategoria.getInstance().listar();
        //2. Creamos modelo
        DefaultTableModel modelo = new DefaultTableModel();
        //3. Agregamos columnas al modelo
        modelo.addColumn("ID");
        modelo.addColumn("Nombre");
        //4. Agregamos filas al modelo
        for (Categoria c : listaCategoria){
            modelo.addRow(new Object[]{
                c.getId(),
                c.getNombre()
            });
        }
        //5. Asignamos el modelo a la tabla
        tblCategoria.setModel(modelo);
    }
